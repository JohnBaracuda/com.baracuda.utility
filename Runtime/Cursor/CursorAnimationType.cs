namespace Baracuda.Bedrock.Cursor
{
    public enum CursorAnimationType
    {
        /// <summary>
        ///     The animation is played once.
        /// </summary>
        PlayOnce,

        /// <summary>
        ///     The animation is played in a continuous loop.
        /// </summary>
        Loop,

        /// <summary>
        ///     the animation is played alternately forwards and backwards in a continuous loop.
        /// </summary>
        PingPong
    }
}